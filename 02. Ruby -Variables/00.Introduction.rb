#2. Ruby variables
    # Global variable - $ sign
    # Available every where within your Ruby script 
        #Có sẵn mỏi nơi trong tệp lệnh Ruby của bạn

    # Local variable
    # It depends on the scope (create new scope with new class, new module, new method)
        #Nó phụ thuộc vào phạm vi (tạo phạm vi mới với lớp mới, mô-đun mới, phương thức mới)

    # Instance variable - @ sign
    # Available only within a specific object, across all methods in a class instance. Not available directly from class definitions.
        #Chỉ có sẵn trong một đối tượng cụ thể, trên tất cả các phương thức trong một thể hiện của lớp. Không có sẵn trực tiếp từ các định nghĩa lớp.

    # Class variable - @@ sign
    # Available from the class definition and any sub-classes. Not available from anywhere outside.
        #Có sẵn từ định nghĩa lớp và bất kỳ lớp con. Không có sẵn từ bất cứ nơi nào bên ngoài
    
    # Constants - Begin with a capital letter  
        #Bắt đầu bằng chữ in hoa
    
