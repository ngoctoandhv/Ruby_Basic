#1. Why Object Oriented Programming?

    #Object Oriented Programming, often referred to as OOP, 
        #is a programming paradigm that was created to deal with the growing complexity 
        #of large software systems. Programmers found out very early on that as applications 
        #grew in complexity and size, they became very difficult to maintain.
        # One small change at any point in the program would trigger a ripple effect of 
        #errors due to dependencies throughout the entire program.

    #Lập trình hướng đối tượng, thường được gọi là OOP, 
        #là một mô hình lập trình được tạo ra để đối phó với sự phức tạp ngày càng tăng của các 
        #hệ thống phần mềm lớn. Các lập trình viên phát hiện ra rất sớm rằng khi các ứng 
        # dụng phát triển phức tạp và kích thước, chúng trở nên rất khó bảo trì.
        # Một thay đổi nhỏ tại bất kỳ điểm nào trong chương trình sẽ gây ra hiệu ứng gợn 
        # lỗi do sự phụ thuộc trong toàn bộ chương trình.

