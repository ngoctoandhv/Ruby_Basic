#Symbol objects represent names and some strings inside the Ruby interpreter
    #đối tượng Symbol tên đại diện và một số chuỗi có trong trình thông dịch ruby
#They are generated using the :name and :"string" literals syntax, and by the to_sym methods.
    #chúng được tạo nên bằng cú pháp :name và :"string" , và cũng bởi phương thức to_sym

#Example symbol:
sym = :hello
sym = :"hello"
sym = "hello".to_sym 
