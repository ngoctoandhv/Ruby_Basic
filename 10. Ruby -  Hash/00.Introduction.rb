# A Hash is a collection of key-value pairs
# It is similar to an Array, except that indexing is done via arbitrary keys of any object type,
    #not an integer index
# Hashes enumerate their values in the order that the corresponding keys were inserted
# Hashes have a default value that is returned when accessing keys that do not exist in the hash.
    # That value is nil by default

# Hash là tập hợp các cặp khóa-giá trị
# Nó tương tự như một mảng, ngoại trừ việc lập chỉ mục được thực hiện thông qua các khóa tùy ý của bất kỳ loại đối tượng nào, 
    # không phải là một chỉ số nguyên
# Hash liệt kê các giá trị của chúng theo thứ tự các khóa tương ứng được chèn vào
# Hash có giá trị mặc định được trả về khi truy cập các khóa không tồn tại trong hàm băm. 
    # Giá trị đó là không theo mặc định

